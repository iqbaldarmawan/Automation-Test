            .checkHighestItemPriceRadioButtonIsDisplayed() 
            .checkAZRadioButtonIsDisplayed()
            .checkZARadioButtonIsDisplayed()
            .clickPopularityRadioButton()
            .checkSortByCriteriaSameAsSelected("Popularity")
    }

